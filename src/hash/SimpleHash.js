'use strict'

class SimpleHash {}

SimpleHash.digest = text => {
  let hash = 0
  if (text.length === 0) {
    return hash
  }
  for (let i = 0; i < text.length; i++) {
    const char = text.charCodeAt(i)
    hash = (hash << 5) - hash + char
    hash = hash & hash
  }
  return hash
}

module.exports = SimpleHash
